const LANGS = ['it', 'en', 'es', 'fr', 'de'];

function buildLangPack(lang) {
  const pack = { ...(COMMON[lang] || COMMON.it) };
  for (const [slug, defs] of Object.entries(PAGE_DEFS)) {
    const d = defs[lang] || defs.it;
    pack[`page.${slug}.title`] = d[0];
    pack[`page.${slug}.subtitle`] = d[1];
    pack[`page.${slug}.docTitle`] = d[2];
  }
  return pack;
}

export const PAGE_I18N = Object.fromEntries(LANGS.map((l) => [l, buildLangPack(l)]));

export function mergePageI18n(appI18n) {
  for (const lang of LANGS) {
    appI18n[lang] = { ...appI18n[lang], ...PAGE_I18N[lang] };
  }
  return appI18n;
}